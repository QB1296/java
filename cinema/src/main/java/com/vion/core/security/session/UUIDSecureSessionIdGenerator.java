package com.vion.core.security.session;

import java.io.Serializable;
import java.util.UUID;

/**
 * <b>功能描述</b> <br>
 * java UUID 生成器
 * @author YUJB
 * @date 2014年10月30日 下午5:30:27
 */
public class UUIDSecureSessionIdGenerator implements SecureSessionIdGenerator{

	@Override
	public Serializable generateId() {
		return  UUID.randomUUID().toString();
	}
	

}
